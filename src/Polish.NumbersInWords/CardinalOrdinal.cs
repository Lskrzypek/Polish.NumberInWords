namespace Polish.NumbersInWords
{
    /// <summary>
    /// Cardinal and ordinal numbers (PL: Liczebniki główne i liczebniki porządkowe)
    /// </summary>
    public enum CardinalOrdinal
    {
        /// <summary>
        /// Cardinal number. For example: one, two, three (PL: liczebnik główny: jeden, dwa, trzy)
        /// </summary>
        Cardinal,

        /// <summary>
        /// Ordinal number. Fro example: first, second, third (PL: liczebnik porządkowy: pierwszy, drugi, trzeci)
        /// </summary>
        Ordinal
    }
}
