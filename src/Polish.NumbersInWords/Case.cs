namespace Polish.NumbersInWords
{
    /// <summary>
    /// Case (PL: Przypadek)
    /// </summary>
    public enum Case
    {
        /// <summary>
        /// Nominative case (PL: Mianownik - kto? co?)
        /// </summary>
        Nominative,

        /// <summary>
        /// Genitive case (PL: Dopełniacz - kogo? czego?)
        /// </summary>
        Genitive,

        /// <summary>
        /// Dative case (PL: Celownik - komu? czemu?)
        /// </summary>
        Dative,

        /// <summary>
        /// Accusative case (PL: Biernik - kogo? co?)
        /// </summary>
        Accusative,

        /// <summary>
        /// Instrumental case (PL: Nadrzędnik - z kim? z czym?)
        /// </summary>
        Instrumental,

        /// <summary>
        /// Locative case (PL: Miejscownik - o kim? o czym?)
        /// </summary>
        Locative,

        /// <summary>
        /// Vocative case (PL: Wołacz)
        /// </summary>
        Vocative
    }
}
